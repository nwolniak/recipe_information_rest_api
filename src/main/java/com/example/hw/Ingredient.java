package com.example.hw;

public class Ingredient {
    private String name;
    private int id;
    private String image;
    private double amount;
    private String unit;

    public String getName() {
        return this.name;
    }

    public int getId() {
        return this.id;
    }

    public String getImage() {
        return this.image;
    }

    public double getAmount() {
        return this.amount;
    }

    public String getUnit() {
        return this.unit;
    }
}
